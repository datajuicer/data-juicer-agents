const DICT: Record<UiLanguage, Dictionary> = {
  en: EN,
  zh: ZH,
};

const I18nContext = createContext<I18nContextValue | null>(null);

function formatTemplate(template: string, params?: TemplateParams): string {
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? ""));
}

function readStoredLanguage(): UiLanguage {
  if (typeof window === "undefined") {
    return "en";
  }
  const value = window.localStorage.getItem(STORAGE_KEY);
  return value === "zh" ? "zh" : "en";
}

export function I18nProvider(props: { children: ReactNode }) {
  const [lang, setLangState] = useState<UiLanguage>(readStoredLanguage);

  const setLang = (next: UiLanguage) => {
    setLangState(next);
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, next);
    }
  };

  const t = useMemo(() => {
    const current = DICT[lang];
    return (key: string, params?: TemplateParams): string => {
      const fallback = DICT.en[key];
      const template = current[key] ?? fallback ?? key;
      return formatTemplate(template, params);
    };
  }, [lang]);

  const value = useMemo(
    () => ({
      lang,
      setLang,
      t,
    }),
    [lang, t],
  );

  return <I18nContext.Provider value={value}>{props.children}</I18nContext.Provider>;
}

export function useI18n(): I18nContextValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error("useI18n must be used under I18nProvider");
  }
  return ctx;
}
